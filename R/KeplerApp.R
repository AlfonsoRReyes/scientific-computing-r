source("./R/ode_generics.R")
source("./R/Kepler.R")
source("./R/RK45.R")

# set the orbit into a predefined state.
r <- c(2, 0)
v <- c(0, 0.25)
dt <- 0.1

planet <- Kepler(r, v)
solver <- RK45(planet)

while (planet@state[5] <= 10) {
    solver <- step(solver)
    planet <- solver@ode
    cat(sprintf("state[1]=%12f, state[2]= %12f,  state[3]=%12f, state[5]=%12f\n", 
                planet@state[1],
                planet@state[2], planet@state[3], planet@state[5]))
}